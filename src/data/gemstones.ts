const heroFor = (image: ProductionImage, alt: string, position = "64% center"): HeroImageConfig => ({
  heroImage: image,
  heroAlt: alt,
  heroObjectPositionDesktop: position,
  heroObjectPositionMobile: "56% center",
  heroOverlayStyle: "light",
  heroTheme: "dark-text",
});

type GemstoneInput = Omit<Gemstone, "care" | "description" | "hero" | "image"> & {
  heroAlt: string;
  heroPosition?: string;
};

function defineGemstone(input: GemstoneInput): Gemstone {
  const { heroAlt, heroPosition, ...gemstone } = input;

  return {
    ...gemstone,
    care: gemstone.careNotes,
    description: gemstone.shortDescription,
    hero: heroFor(gemstone.heroImage, heroAlt, heroPosition),
    image: gemstone.primaryImage,
  };
}

