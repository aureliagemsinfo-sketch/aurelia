export function getGemstone(slug: string) {
  return gemstones.find((gemstone) => gemstone.slug === slug);
}

export function getFeaturedGemstones(limit?: number) {
  const featured = gemstones
    .filter((gemstone) => gemstone.isFeatured)
    .sort((a, b) => a.displayOrder - b.displayOrder);
  return typeof limit === "number" ? featured.slice(0, limit) : featured;
}

export function getGemstonesByCollection(collectionSlug: string) {
  return gemstones.filter((gemstone) => gemstone.relatedCollectionSlugs.includes(collectionSlug));
}

export function getRelatedGemstones(gemstone: Gemstone) {
  return gemstone.relatedGemSlugs
    .map((slug) => getGemstone(slug))
    .filter((relatedGemstone): relatedGemstone is Gemstone => Boolean(relatedGemstone));
}
